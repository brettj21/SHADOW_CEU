<!--                        As an APA-approved sponsor of continuing education, CEUnits is committed to the identification and resolution of potential conflicts of interest in the planning, promotion, delivery, and evaluation of continuing education. Consistent with concepts outlined in the APA Ethical Principles of Psychologists and Code of Conduct, potential conflicts of interest occur when an individual assumes a professional role in the planning, promotion, delivery, or evaluation of continuing education where personal, professional, legal, financial, or other interests could reasonably be expected to impair his or her objectivity, competence, or effectiveness. -->
                        <b>Potential Conflicts of Interest in the Planning of Continuing Education</b><br />
                        Dan Rebek is responsible to identify, declare, and resolve any potential conflict of interest CEUnits may have in offering any specific program of continuing education. In the event of a potential conflict of interest, Dan Rebek will assume responsibility to resolve the potential conflict. In the event of a potential conflict of interest, Dan Rebek will ensure that the involved individual recuses him or herself from the planning process. If Dan Rebek has a conflict of interest another staff member will be assigned to address the conflict of interest.
                        <b>Potential Conflicts of Interest in the Promotion of Continuing Education</b><br />
                        When preparing promotional material for continuing education, CEUnits includes information concerning any financial support, including in-kind support, provided by another party. CEUnits also informs potential participants of any potential conflicts of interest an instructor. If there is no financial support or any potential conflict of interest or commercial support, CEUnits will ensure that is clearly stated in the promotional material.
                        <b>Potential Conflicts of Interest in the Delivery of Continuing Education</b><br />
                        CEUnits requires that there is a process to clearly describe any financial support for a continuing education program at the time the continuing education program begins. Any other relationship that could be reasonably construed as a conflict of interest will also be disclosed. If there is no financial support or potential conflict of interest, CEUnits will ensure there is a process (e.g., a designated individual or a slide at the start of the presentation, documentation in materials) to clearly state any potential conflicts at the time the continuing education program begins. If the presenter is receiving participant fees for providing the training, participants are made aware of this prior to the training.
